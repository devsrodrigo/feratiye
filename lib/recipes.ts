export type Category =
  | 'Desayunos'
  | 'Botanas'
  | 'Salsas y Guarniciones'
  | 'Arroz'
  | 'Casera'
  | 'Cocina Italiana'
  | 'Cocina Asiática'
  | 'Cocina Americana'
  | 'Mariscos'
  | 'Saludable'
  | 'Bebidas'
  | 'Postres';

export type LegacyCategory =
  | 'Dips y Aderezos'
  | 'Salsas y Acompañamientos'
  | 'Drinks'
  | 'Pastas'
  | 'Pescado'
  | 'Comida Casera'
  | 'Comida Asiática'
  | 'Comida Americana'
  | 'Arroz';

export type RecipeCategory = Category | LegacyCategory;

export interface Recipe {
  title: string;
  slug: string;
  category: RecipeCategory;
  description: string;
  image: string;
  tiktokUrl: string;
  ingredients: string[];
  steps: string[];
  featured?: boolean;
}

export function generateSlug(text: string): string {
  return text
    .toLowerCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/[^a-z0-9\s-]/g, '')
    .trim()
    .replace(/\s+/g, '-');
}

